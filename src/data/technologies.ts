// Helper function to get technology by value
export const getTechnologyByValue = (value: string): Technology | undefined => {
  return sampleTechnologies.find((tech) => tech.value === value);
};

// Helper function to get technology by label
export const getTechnologyByLabel = (label: string): Technology | undefined => {
  return sampleTechnologies.find((tech) => tech.label === label);
};

// Helper function to get all technology values (for project forms)
